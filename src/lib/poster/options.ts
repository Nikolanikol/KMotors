// Кураторские «изюминки» комплектации для подписи поста.
// Из полного списка опций авто выбираем самые продающие фичи и показываем их
// короткими ярлыками, по одной на строку (полный список — на странице авто KMotors).
// Коды соответствуют словарю OptionsRow (data.ts) — единый источник правды.

interface Highlight {
  codes: string[]; // если у авто есть любой из кодов — показываем label один раз
  label: string;
}

