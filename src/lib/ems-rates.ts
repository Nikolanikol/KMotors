// ─── Core calculation ─────────────────────────────────────────────────────────

/**
 * Returns EMS shipping cost in KRW, or null if EMS is not available.
 * @param countryCode ISO 3166-1 alpha-2 code (e.g. "RU")
 * @param billedWeightKg billed weight in kg (packed_weight_kg for EMS)
 */
export function calcEmsKrw(
  countryCode: string,
  billedWeightKg: number
): number | null {
  const rateKey = COUNTRY_RATE[countryCode];
  if (!rateKey || rateKey === "X") return null;

  const rates = RATES_KRW[rateKey];
  if (!rates) return null;

  // Round up to next weight step
  const stepIndex = WEIGHT_STEPS.findIndex((s) => s >= billedWeightKg);
  if (stepIndex === -1) return null; // over 30 kg — EMS not applicable

  return rates[stepIndex];
}

/**
 * Returns EMS shipping cost in USD.
 * @param krwToUsd exchange rate (e.g. 0.00073 for 1 KRW = 0.00073 USD)
 */
export function calcEmsUsd(
  countryCode: string,
  billedWeightKg: number,
  krwToUsd: number
): number | null {
  const krw = calcEmsKrw(countryCode, billedWeightKg);
  if (krw === null) return null;
  return Math.ceil(krw * krwToUsd);
}

export function isEmsAvailable(countryCode: string): boolean {
  const key = COUNTRY_RATE[countryCode];
  return !!key && key !== "X";
}

