/**
 * Returns EMS Premium shipping cost in KRW, or null if not available / over 20kg.
 * Billing uses GREATEST(packed_weight, vol_weight).
 */
export function calcEmspKrw(
  countryCode: string,
  billedWeightKg: number
): number | null {
  const rateKey = EMSP_COUNTRY_RATE[countryCode];
  if (!rateKey || rateKey === "X") return null;

  const rates = EMSP_RATES_KRW[rateKey];
  if (!rates) return null;

  const stepIndex = EMSP_WEIGHT_STEPS.findIndex((s) => s >= billedWeightKg);
  if (stepIndex === -1) return null; // over 20 kg — not in this table

  return rates[stepIndex];
}

export function calcEmspUsd(
  countryCode: string,
  billedWeightKg: number,
  krwToUsd: number
): number | null {
  const krw = calcEmspKrw(countryCode, billedWeightKg);
  if (krw === null) return null;
  return Math.ceil(krw * krwToUsd);
}

export function isEmspAvailable(countryCode: string): boolean {
  const key = EMSP_COUNTRY_RATE[countryCode];
  return !!key && key !== "X";
}
